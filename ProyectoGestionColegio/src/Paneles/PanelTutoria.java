    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        if (!jTextFieldBuscarAlu.getText().isEmpty()){
            String genero;
            String idB = jTextFieldBuscarAlu.getText();
            int pos = Colegio.listaAula[pA].buscarIDAlumno(idB);  
            int posAlu;
            if(pos>=0){
                posAlu = Colegio.existeBuscarAlu(idB);
                this.jTextIdAlumno.setText(Colegio.listaAlu[posAlu].getCodigo());
                this.jTextApellidoAlu.setText(Colegio.listaAlu[posAlu].getApellido());
                this.jTextNombreAlu.setText(Colegio.listaAlu[posAlu].getNombre());
                this.jTextDniAlu.setText(Colegio.listaAlu[posAlu].getDni());
                genero = Colegio.listaAlu[posAlu].getGenero();
                if(genero.equals("Femenino")){
                    jRadioButtonFemenino.setSelected(true);
                }else{
                    jRadioButtonMasculino.setSelected(true);
                }
                this.jTextDireccionAlu.setText(Colegio.listaAlu[posAlu].getDomicilio());

                this.jSpinnerDiaNac.setValue(Colegio.listaAlu[posAlu].getDiaNac());
                this.jSpinnerMesNac.setValue(Colegio.listaAlu[posAlu].getMesNac());
                this.jSpinnerAnioNac.setValue(Colegio.listaAlu[posAlu].getAnioNac());
            }else{
                JOptionPane.showMessageDialog(null, "Alumno con id " + idB + " NO encontrado" );
            }
        }else{
            JOptionPane.showMessageDialog(null, "Espacio Vacio");
        }     
    }//GEN-LAST:event_jButtonBuscarActionPerformed
    
