    public void mostrarTabla(){
        
        String alus[] = Colegio.listaAula[pA].getAlumnos();
        int posAlu;
        int cantidadAlumnos = alus.length;
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("Id");
        modelo.addColumn("Apellidos");
        modelo.addColumn("Nombres");
        modelo.addColumn("DNI");
        modelo.addColumn("Genero");
        modelo.addColumn("Edad");
        
        jTableAlumnos.setModel(modelo);
        
         String datos[] = new String[6];
         
         for(int i=0;i<cantidadAlumnos;i++){
             posAlu = Colegio.existeBuscarAlu(alus[i]);
            datos[0]= Colegio.listaAlu[posAlu].getCodigo();
            datos[1]=Colegio.listaAlu[posAlu].getApellido();
            datos[2]=Colegio.listaAlu[posAlu].getNombre();
            datos[3]=Colegio.listaAlu[posAlu].getDni();
            datos[4]=Colegio.listaAlu[posAlu].getGenero();
            datos[5]=Colegio.listaAlu[posAlu].calcEdad() + "";
            modelo.addRow(datos);            
        }      
    }
     
