    private void jButtonActualizarAluActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActualizarAluActionPerformed
        int dia,mes,anio;
        Alumno a=new Alumno();
        String id = jTextIdAlumno.getText();
        if (!jTextNombreAlu.getText().isEmpty() && !jTextApellidoAlu.getText().isEmpty()
                && !jTextDniAlu.getText().isEmpty() && !jTextDireccionAlu.getText().isEmpty() && 
                (jRadioButtonFemenino.isSelected()||jRadioButtonMasculino.isSelected())){
            //int pos = Colegio.existeBuscarAlu(id);
                a.setCodigo(id);
                a.setNombre(jTextNombreAlu.getText());

                a.setApellido(jTextApellidoAlu.getText());
                try{
                    int dni=Integer.parseInt(jTextDniAlu.getText());
                    a.setDni(jTextDniAlu.getText());
                    //Validar numero de DNI Cantidad de dígitos 
                    a.setDomicilio(jTextDireccionAlu.getText());
                    dia = Integer.parseInt(jSpinnerDiaNac.getValue().toString());
                    a.setDiaNac(dia);

                    mes = Integer.parseInt(jSpinnerMesNac.getValue().toString());
                    a.setMesNac(mes);

                    anio = Integer.parseInt(jSpinnerAnioNac.getValue().toString());
                    a.setAnioNac(anio);

                    if(jRadioButtonFemenino.isSelected()){
                        a.setGenero(""+"F");
                    }else{
                        if(jRadioButtonMasculino.isSelected()){
                            a.setGenero(""+"M");
                        }
                    }
                    bdAlumno.actualizarAlumno(a);
                    JOptionPane.showMessageDialog(null, "Alumno editado correctamente");
                    mostrarTabla();
                    limpiar();
                }catch(NumberFormatException ev){
                    JOptionPane.showMessageDialog(null, "Ingrese un DNI válido");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Faltan Datos");
            }
    }//GEN-LAST:event_jButtonActualizarAluActionPerformed

