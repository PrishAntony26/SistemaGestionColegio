public void mostrarTabla(){
        DefaultTableModel modelo = new DefaultTableModel();
        Alumno listaAlu[] = bdAlumno.obtenerAlumnos();
        modelo.addColumn("Id");
        modelo.addColumn("Aula");
        modelo.addColumn("Apellidos");
        modelo.addColumn("Nombre");
        modelo.addColumn("DNI");
        modelo.addColumn("Genero");
        //modelo.addColumn("Celular");
        jTableAlumnos.setModel(modelo);
         String datos[] = new String[6];
         
         System.out.println();
         cant=0;
         while(listaAlu[cant]!=null){
            String cad = listaAlu[cant].getCodigo();
            datos[0]= cad;
            System.out.println(cad);
            cad = listaAlu[cant].getCodA();
            datos[1]= cad;
            cad = listaAlu[cant].getApellido();
            datos[2]= cad;
            cad = listaAlu[cant].getNombre();
            datos[3]= cad;
            cad = listaAlu[cant].getDni();
            datos[4]= cad;
            cad = listaAlu[cant].getGenero();
            datos[5]= cad;
            //datos[5]=Colegio.listaProf[i].getCelular();
            modelo.addRow(datos);
            cant++;
        }    
        jTableAlumnos.setModel(modelo);
        
    }
     
